    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String regno = jComboBox1.getSelectedItem().toString();
          try {
    
    Connection con = ConnectionProvider.getCon();
    
    
    String sql = "DELETE FROM padmin WHERE username = ?";
    
   
    PreparedStatement pr = con.prepareStatement(sql);
    
    
    pr.setString(1, regno); 
    
    
    int rowsAffected = pr.executeUpdate();
    
    
    if (rowsAffected > 0) {
        JOptionPane.showMessageDialog(null, "Teacher with username  '"+regno+"' deleted successfully.");
        dispose();
        new adminindex().setVisible(true);
    } else {
        JOptionPane.showMessageDialog(null, "Teacher with username '"+regno+"' not founda");
        dispose();
        new adminindex().setVisible(true);
    }

    // Close resources
    pr.close();
    con.close();
} catch (Exception e) {
    e.printStackTrace();
}

        
        
    }//GEN-LAST:event_jButton1ActionPerformed

