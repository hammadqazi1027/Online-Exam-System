    public deleteteacher() {
        initComponents();
        
        try {
            // Establish database connection
            Connection con = ConnectionProvider.getCon();

            // SQL query to fetch student names
            String query = "SELECT username FROM padmin";
            PreparedStatement statement = con.prepareStatement(query);

            // Execute query
            ResultSet resultSet = statement.executeQuery();

            // Populate the dropdown list
            while (resultSet.next()) {
                String studentName = resultSet.getString("username");
                jComboBox1.addItem(studentName);
            }

            // Close resources
            
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error fetching student data: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

